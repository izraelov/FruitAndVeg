public class Guest {
    final private String name = "Guest";
    final private String password = null;
    final private String Phone = null;
    final private String isStaff = "false";
    final private String image = null;
    final private String securecode = null;


public Guest()
{

}

    public String getName() {
        return name;
    }

    public String getPassword() {
        return password;
    }

    public String getPhone() {
        return Phone;
    }

    public String getIsStaff() {
        return isStaff;
    }

    public String getImage() {
        return image;
    }

    public String getSecurecode() {
        return securecode;
    }
}
